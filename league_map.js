// 反向映射（500.com名称 -> 竞彩名称）
const REVERSE_NAME_MAP = {};
for (const [key, value] of Object.entries(LEAGUE_NAME_MAP)) {
  if (key !== value) {
    REVERSE_NAME_MAP[value] = key;
  }
}

// 联赛ID映射（联赛名称 -> 500.com ID）
const LEAGUE_ID_MAP = {};
for (const league of ALL_LEAGUES) {
  const normalizedName = LEAGUE_NAME_MAP[league.name] || league.name;
  LEAGUE_ID_MAP[normalizedName] = league.id;
  // 也支持直接用500.com名称查ID
  LEAGUE_ID_MAP[league.name] = league.id;
}

/**
 * 模糊匹配联赛名称
 * @param {string} name1 - 联赛名称1
 * @param {string} name2 - 联赛名称2
 * @returns {boolean} - 是否匹配
 */
function leagueMatch(name1, name2) {
  if (!name1 || !name2) return false;
  if (name1 === name2) return true;
  
  // 标准化处理
  const n1 = name1.replace(/联赛|超级联赛|甲级联赛|乙级联赛|杯/g, '').trim();
  const n2 = name2.replace(/联赛|超级联赛|甲级联赛|乙级联赛|杯/g, '').trim();
  if (n1 === n2) return true;
  
  // 检查映射
  const mapped1 = LEAGUE_NAME_MAP[name1];
  const mapped2 = LEAGUE_NAME_MAP[name2];
  if (mapped1 && mapped2 && mapped1 === mapped2) return true;
  if (mapped1 && mapped1 === name2) return true;
  if (mapped2 && mapped2 === name1) return true;
  
  // 包含匹配
  if (n1.includes(n2) || n2.includes(n1)) return true;
  
  return false;
}

/**
 * 根据名称查找联赛ID
 * @param {string} name - 联赛名称
 * @returns {string|null} - 联赛ID
 */
function getLeagueId(name) {
  if (!name) return null;
  
  // 直接查找
  if (LEAGUE_ID_MAP[name]) return LEAGUE_ID_MAP[name];
  
  // 映射查找
  const mapped = LEAGUE_NAME_MAP[name];
  if (mapped && LEAGUE_ID_MAP[mapped]) return LEAGUE_ID_MAP[mapped];
  
  // 模糊查找
  for (const [key, id] of Object.entries(LEAGUE_ID_MAP)) {
    if (leagueMatch(name, key)) return id;
  }
  
  return null;
}

/**
 * 标准化联赛名称
 * @param {string} name - 联赛名称
 * @returns {string} - 标准化后的名称
 */
function normalizeLeagueName(name) {
  if (!name) return '未知';
  return LEAGUE_NAME_MAP[name] || name;
}

